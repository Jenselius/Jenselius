// Current language state
let currentLanguage = 'en';

// Language switching functionality
function switchLanguage(lang) {
    console.log('Switching to language:', lang);
    
    if (!translations[lang]) {
        console.error('Translation not found for language:', lang);
        return;
    }
    
    currentLanguage = lang;
    
    // Update language buttons
    document.querySelectorAll('.language-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });
    
    // Update all translatable elements
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update privacy policy link based on language
    const privacyLink = document.querySelector('a[href*="privacy"]');
    if (privacyLink) {
        if (lang === 'de') {
            privacyLink.href = 'datenschutz.html';
        } else {
            privacyLink.href = 'privacy-policy.html';
        }
    }
    
    // Update placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[lang][key]) {
            element.setAttribute('placeholder', translations[lang][key]);
        }
    });
    
    // Update select options
    document.querySelectorAll('option[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update document language attribute
    document.documentElement.lang = lang;
    
    // Store language preference
    localStorage.setItem('preferred-language', lang);
    
    console.log('Language switch completed for:', lang);
}

// Contact form handling
function handleContactForm() {
    const form = document.getElementById('contactForm');
    if (!form) return;
    
    form.addEventListener('submit', function(e) {
        e.preventDefault();
        
        const formData = new FormData(form);
        const name = formData.get('name');
        const email = formData.get('email');
        const message = formData.get('message');
        
        // Basic validation
        if (!name || !email || !message) {
            alert(currentLanguage === 'de' ? 'Bitte füllen Sie alle Pflichtfelder aus.' : 'Please fill in all required fields.');
            return;
        }
        
        // Email validation
        const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!emailRegex.test(email)) {
            alert(currentLanguage === 'de' ? 'Bitte geben Sie eine gültige E-Mail-Adresse ein.' : 'Please enter a valid email address.');
            return;
        }
        
        // Success message
        alert(currentLanguage === 'de' ? 
              'Vielen Dank für Ihre Nachricht! Ich werde mich bald bei Ihnen melden.' :
              'Thank you for your message! I will get back to you soon.');
        
        form.reset();
    });
}

// Resources form handling
function handleResourcesForm() {
    const form = document.getElementById('resourcesForm');
    const thankYouSection = document.getElementById('thankYouSection');
    
    if (!form || !thankYouSection) {
        return;
    }
    
    form.addEventListener('submit', function(e) {
        e.preventDefault();
        
        // Get email input
        const emailInput = form.querySelector('input[type="email"]');
        const email = emailInput ? emailInput.value.trim() : '';
        
        // Improved email validation
        const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!email || !emailRegex.test(email)) {
            const errorMessage = currentLanguage === 'de' ? 
                'Bitte geben Sie eine gültige E-Mail-Adresse ein.' : 
                'Please enter a valid email address.';
            alert(errorMessage);
            return;
        }
        
        // Hide form and show thank you section
        form.style.display = 'none';
        thankYouSection.classList.remove('hidden');
        
        // Scroll to thank you section smoothly
        thankYouSection.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
        
        // Store email for analytics
        const accessData = {
            email: email,
            timestamp: new Date().toISOString(),
            language: currentLanguage
        };
        localStorage.setItem('resource_access', JSON.stringify(accessData));
    });
}

// Smooth scrolling for anchor links
function setupSmoothScrolling() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
}

// Initialize website functionality
function initializeWebsite() {
    // Check for saved language preference
    const savedLanguage = localStorage.getItem('preferred-language');
    if (savedLanguage && translations[savedLanguage]) {
        switchLanguage(savedLanguage);
    } else {
        // Default to English
        switchLanguage('en');
    }
    
    // Set up forms
    handleContactForm();
    handleResourcesForm();
    
    // Set up smooth scrolling
    setupSmoothScrolling();
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', initializeWebsite);

// Export functions for global access
window.switchLanguage = switchLanguage;