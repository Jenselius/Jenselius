// Current language state
let currentLanguage = 'en';

// Language switching functionality
function switchLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        
        // Update language buttons
        document.querySelectorAll('.language-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === lang) {
                btn.classList.add('active');
            }
        });
        
        // Update all translatable elements
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[lang][key]) {
                element.textContent = translations[lang][key];
            }
        });
        
        // Update privacy policy link based on language
        const privacyLink = document.querySelector('a[href*="privacy"]');
        if (privacyLink) {
            if (lang === 'de') {
                privacyLink.href = 'datenschutz.html';
            } else {
                privacyLink.href = 'privacy-policy.html';
            }
        }
        
        // Update placeholders
        document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            if (translations[lang][key]) {
                element.setAttribute('placeholder', translations[lang][key]);
            }
        });
        
        // Update select options
        document.querySelectorAll('option[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[lang][key]) {
                element.textContent = translations[lang][key];
            }
        });
        
        // Update document language attribute
        document.documentElement.lang = lang;
        
        // Store language preference
        localStorage.setItem('preferred-language', lang);
    }
}

// FAQ toggle functionality
function toggleFaq(element) {
    const faqAnswer = element.nextElementSibling;
    const chevron = element.querySelector('i');
    
    if (faqAnswer.classList.contains('hidden')) {
        faqAnswer.classList.remove('hidden');
        chevron.style.transform = 'rotate(180deg)';
    } else {
        faqAnswer.classList.add('hidden');
        chevron.style.transform = 'rotate(0deg)';
    }
}

// Contact form handling
function handleContactForm() {
    const form = document.getElementById('contactForm');
    
    form.addEventListener('submit', function(e) {
        e.preventDefault();
        
        // Get form data
        const formData = {
            name: document.getElementById('name').value,
            email: document.getElementById('email').value,
            company: document.getElementById('company').value,
            package: document.getElementById('package').value,
            message: document.getElementById('message').value,
            language: currentLanguage
        };
        
        // Basic validation
        if (!formData.name || !formData.email || !formData.message) {
            alert(currentLanguage === 'de' ? 'Bitte füllen Sie alle Pflichtfelder aus.' :
                  'Please fill in all required fields.');
            return;
        }
        
        // Email validation
        const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!emailRegex.test(formData.email)) {
            alert(currentLanguage === 'de' ? 'Bitte geben Sie eine gültige E-Mail-Adresse ein.' :
                  'Please enter a valid email address.');
            return;
        }
        
        // Success message
        alert(currentLanguage === 'de' ? 
              'Vielen Dank für Ihre professionelle Anfrage! Ich melde mich innerhalb von 24 Stunden bei Ihnen.' :
              'Thank you for your professional inquiry! I\'ll respond within 24 hours.');
        
        // Reset form
        form.reset();
        
        // In a real implementation, you would send this data to your backend
        console.log('Professional inquiry submitted:', formData);
    });
}

// Smooth scrolling for anchor links
function setupSmoothScrolling() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
}

// Initialize website functionality
function initializeWebsite() {
    // Check for saved language preference
    const savedLanguage = localStorage.getItem('preferred-language');
    if (savedLanguage && translations[savedLanguage]) {
        switchLanguage(savedLanguage);
    } else {
        // Default to English
        switchLanguage('en');
    }
    
    // Set up contact form
    handleContactForm();
    
    // Set up smooth scrolling
    setupSmoothScrolling();
    
    // Add scroll effect to header
    window.addEventListener('scroll', function() {
        const header = document.querySelector('header');
        if (window.scrollY > 50) {
            header.style.background = 'rgba(15, 23, 42, 0.98)';
        } else {
            header.style.background = 'rgba(15, 23, 42, 0.95)';
        }
    });
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', initializeWebsite);

